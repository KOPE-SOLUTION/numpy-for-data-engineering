import numpy as np

print("=== EP2 Demo: The Basics of NumPy Arrays ===")

np.random.seed(0)
print("\n1) Attributes")
x1 = np.random.randint(10, size=6)
x2 = np.random.randint(10, size=(3,4))
x3 = np.random.randint(10, size=(3,4,5))
print("x1 =", x1)
print("x2 =\n", x2)
print("x3.shape =", x3.shape)
print("x3.ndim =", x3.ndim)
print("x3.size =", x3.size)
print("x3.dtype =", x3.dtype)
print("x3.itemsize =", x3.itemsize)
print("x3.nbytes =", x3.nbytes)

print("\n2) Indexing")
x1 = np.array([5, 0, 3, 3, 7, 9])
x2 = np.array([[3,5,2,4],[7,6,8,8],[1,6,7,7]])
print("x1[0] =", x1[0])
print("x1[-1] =", x1[-1])
print("x2[0,0] =", x2[0,0])
print("x2[2,-1] =", x2[2,-1])

print("\n3) Slicing")
x = np.arange(10)
print("x =", x)
print("x[:5] =", x[:5])
print("x[5:] =", x[5:])
print("x[::2] =", x[::2])
print("x[::-1] =", x[::-1])

print("\n4) View vs Copy")
x2 = np.array([[12,5,2,4],[7,6,8,8],[1,6,7,7]])
x2_sub = x2[:2, :2]
x2_sub[0,0] = 99
print("x2 after view edit =\n", x2)
x2 = np.array([[12,5,2,4],[7,6,8,8],[1,6,7,7]])
x2_copy = x2[:2, :2].copy()
x2_copy[0,0] = 42
print("x2_copy =\n", x2_copy)
print("x2 original =\n", x2)

print("\n5) Reshape")
print(np.arange(1, 10).reshape((3,3)))
x = np.array([1,2,3])
print(x.reshape((1,3)))
print(x.reshape((3,1)))
print(x[np.newaxis, :])
print(x[:, np.newaxis])

print("\n6) Concatenate")
x = np.array([1,2,3])
y = np.array([[99],[99]])
grid = np.array([[9,8,7],[6,5,4]])
print(np.concatenate([x, x]))
print(np.vstack([x, grid]))
print(np.hstack([grid, y]))

print("\n7) Split")
x = np.array([1,2,3,99,99,3,2,1])
x1, x2, x3 = np.split(x, [3, 5])
print("x1 =", x1, "x2 =", x2, "x3 =", x3)
grid = np.arange(16).reshape((4,4))
upper, lower = np.vsplit(grid, [2])
left, right = np.hsplit(grid, [2])
print("upper =\n", upper)
print("lower =\n", lower)
print("left =\n", left)
print("right =\n", right)
